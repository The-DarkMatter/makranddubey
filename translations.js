// ========================================
// Language Management
// ========================================

let currentLanguage = 'hi'; // Default language

// Get current language
function getCurrentLanguage() {
    return currentLanguage;
}

// Set language
function setLanguage(lang) {
    if (lang !== 'hi' && lang !== 'en') {
        console.error('Invalid language. Use "hi" or "en".');
        return;
    }
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    updatePageLanguage();
}

// Get translation for a key
function translate(key) {
    return translations[currentLanguage][key] || key;
}

// Update all elements with data-translate attribute
function updatePageLanguage() {
    const elementsToTranslate = document.querySelectorAll('[data-translate]');
    
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = translate(key);
        
        // Update text content for most elements
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else if (element.hasAttribute('aria-label')) {
            element.setAttribute('aria-label', translation);
        } else {
            element.textContent = translation;
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLanguage;
    
    // Update button text
    const langToggleBtn = document.querySelector('.lang-toggle-btn');
    if (langToggleBtn) {
        langToggleBtn.textContent = currentLanguage === 'hi' ? 'English' : 'हिन्दी';
    }
    
    // Dispatch custom event for any additional updates
    document.dispatchEvent(new CustomEvent('languageChanged', { 
        detail: { language: currentLanguage } 
    }));
}

// Initialize language on page load
function initLanguage() {
    // Check for saved preference
    const savedLang = localStorage.getItem('preferredLanguage');
    if (savedLang) {
        currentLanguage = savedLang;
    }
    updatePageLanguage();
}

// Run on DOM ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLanguage);
} else {
    initLanguage();
}
